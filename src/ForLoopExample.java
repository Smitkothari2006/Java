public class ForLoopExample {
    public static void main(String[] args) {
        System.out.println("Program to explain For Loop ");
        System.out.println("Program to print first 10 Natural numbers using increment for loop ");

        for (int i = 1;i<=10;i++){
            System.out.println(i);
        }
        System.out.println("Program to print 5---4---3---2---1---0 reverse sequence  using decrement for loop ");
        for(int a = 5;a!=0;a--){
            System.out.println(a);
        }
    }
}
