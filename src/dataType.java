public class dataType {
    public static void main(String[] args) {
        // Initialization of variable
        // Syntax: dataType identifier = literal;
        // If direct values are used, they are known as literals.
        // int myNumber = 7;

        // Declaration and assignment
        int yourNumber; // Declaration
        yourNumber = 10; // Initialization
        System.out.println("Your Integer Number is " + yourNumber);

        // Updating the variable
        yourNumber = 90;
        System.out.println("Your Updated Integer Number is " + yourNumber);

        // Float and Double values
        float myFloat = 7.077f; // 'f' is required for float literals
        double myDouble = 7.00000077777;
        System.out.println("Your Float Number is " + myFloat);
        System.out.println("Your Double Number is " + myDouble);

        // Boolean value
        boolean myBool = true;
        System.out.println("Your Boolean is " + myBool);

        // String value
        String myString = "Smit Kothari";
        System.out.println("Your String is " + myString);
    }
}
